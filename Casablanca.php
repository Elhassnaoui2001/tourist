                                <h5 class="mb-3">Cathédrale de Casablanca (Sacré-Cœur)</h5>
                            <img class="img-fluid w-100" src="img/egliscasa.jpg">
<p>The Cathédrale de Casablanca, also known as Sacré-Cœur, is a magnificent architectural gem located in the heart of Casablanca, Morocco. This imposing cathedral stands as a testament to the city's rich history and the influence of French colonial architecture in the region.

Constructed in the early 20th century, the Cathédrale de Casablanca showcases a blend of neo-Gothic and Art Deco architectural styles. Its striking façade features intricate stone carvings, towering spires, and stained glass windows that add to its grandeur and beauty.

Although no longer an active place of worship, the cathedral's historic significance and architectural allure continue to captivate visitors. The interior of the cathedral, with its high vaulted ceilings, intricate arches, and ornate detailing, offers a glimpse into its past splendor.

Today, the Cathédrale de Casablanca serves as a cultural space that occasionally hosts art exhibitions, musical performances, and cultural events. Its spacious interior provides a unique setting for these gatherings, evoking a sense of reverence and tranquility.

The cathedral's central location makes it easily accessible for visitors to explore. Its proximity to other iconic landmarks, such as the Hassan II Mosque and the Casablanca Medina, allows for a captivating architectural and cultural tour of the city.

The Cathédrale de Casablanca stands as a symbol of Casablanca's diverse cultural heritage and serves as a reminder of the city's colonial past. Whether admired from its exterior or explored within, this architectural marvel leaves a lasting impression on those who have the opportunity to witness its grandeur.</p>
