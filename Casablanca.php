                            <h4 class="mb-3">Hassan II Mosque</h4>
                            <img class="img-fluid w-100" src="img/mosquee-hassan-II.jpg">
                            <p>This magnificent mosque is not only a place of worship but also an architectural masterpiece. It is one of the largest and most beautiful mosques in the world. Admire its meticulous design, exquisite details, and impressive waterfront location. A guided tour will allow you to explore the grand prayer hall, stunning tiles, and breathtaking views.
                            This opulent mosque, built at enormous expense, is set on an outcrop jutting over the ocean with a 210m-tall minaret that's a city landmark. It's a showcase of the finest Moroccan artisanship: hand-carved stone and wood, intricate marble flooring and inlay, gilded cedar ceilings and exquisite zellige (geometric mosaic tilework) abound. It's one of two Moroccan mosques open to non-Muslims; multilanguage guided tours are conducted outside prayer times for modestly clad visitors. There’s also a small museum showcasing the craftwork involved.

One of the largest mosques in Africa, it can hold 105,000 worshippers – 25,000 inside, the rest on the outside courtyards. Built and partially funded by King Hassan II (the remaining funds were gathered through a somewhat controversial public subscription process), the mosque complex was designed by French architect Michel Pinseau, took six years to build and was completed in 1993.    
                            <br>
                                <h4 class="mb-3">Old Medina</h4>
                            <img class="img-fluid w-100" src="img/oldmdinacasa.webp">
                            <p>Immerse yourself in the past as you wander through the narrow, winding alleys of Casablanca's Old Medina. Although smaller than other medinas in Morocco, it retains its authentic charm and offers a glimpse into traditional Moroccan life. Explore bustling markets, discover local craftsmanship, and savor the flavors of traditional cuisine.
                            While the 18th century Medina of Casablanca is far eclipsed by its counterparts across the country, it is by no means a skippable attraction. In contrary to other Medinas in Morocco, the city’s historic district is devoid of any tourist-oriented sites and facilities. In fact, foreign visitors, especially the camera-armed ones, are greeted with a great deal of suspicion. This unwelcoming attitude should not deter you from going there, but rather be taken into account upon visit. Therefore, asking people for a permission before taking photos of them or their property is highly recommended.
                            </p>
