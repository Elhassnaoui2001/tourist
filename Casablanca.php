                                <h5 class="mb-3">"Mahkama du Pacha"</h5>
                            <img class="img-fluid w-100" src="img/mhkamaelbachacasa.jfif">

<p>
The Mahkama du Pacha, located in Casablanca, is a remarkable architectural gem that serves as the historic courthouse and administrative building of the city. Also known as the Pacha's Court, it stands as a testament to Morocco's rich cultural heritage and traditional craftsmanship.

Constructed in the early 20th century, the Mahkama du Pacha showcases stunning Moorish and Moroccan architectural styles. The building's exterior is adorned with intricate geometric patterns, ornate woodwork, and colorful tilework, reflecting the country's rich decorative traditions.

As you enter the Mahkama du Pacha, you are greeted by a grand courtyard featuring a central fountain and surrounded by intricately carved wooden screens. The interior of the building is equally impressive, with each room exuding a sense of opulence and splendor. Elaborate zellij tilework, hand-carved plasterwork, and exquisite wood paneling adorn the walls and ceilings, creating a captivating ambiance.

The Mahkama du Pacha was historically used as a court of law, where judgments were passed and disputes were resolved. The building's main courtroom, adorned with its ornate carved ceiling and intricate chandeliers, remains a centerpiece of the complex. Today, it is primarily a tourist attraction, allowing visitors to appreciate the architectural beauty and historical significance of the building.

Guided tours of the Mahkama du Pacha offer a glimpse into Morocco's legal and administrative systems, as well as the opportunity to marvel at the intricate craftsmanship and stunning architectural details. The building provides a rich backdrop for learning about Moroccan culture, art, and history.

Located in the heart of Casablanca, the Mahkama du Pacha stands as a remarkable symbol of the city's cultural heritage and architectural prowess. Its blend of Moorish and Moroccan design elements creates an awe-inspiring experience for visitors, transporting them to a bygone era of grandeur and elegance.    
                        </p>                             
                        <h5 class="mb-3">"Place Mohammed V"</h5>
                            <img class="img-fluid w-100" src="img/placecasa.jpg">

<p>
Place Mohammed V, located in the heart of Casablanca, is a bustling square that serves as a vibrant hub of activity and a significant landmark in the city. Named after King Mohammed V, the square is a testament to Morocco's modern history and its commitment to progress.

The square's design reflects a harmonious blend of Moroccan and French architectural influences. It features a central fountain surrounded by meticulously manicured gardens, offering a serene and picturesque setting. The open space and tree-lined pathways provide a pleasant area for locals and visitors to relax, stroll, and enjoy the vibrant atmosphere.

Flanking the square are several important buildings that add to its grandeur. The Wilaya (Governor's Office), a striking example of Moorish architecture, stands tall with its elegant arches and intricate detailing. The historic courthouse, known as the Palais de Justice, showcases an impressive façade and stands as a symbol of the city's legal system.

Place Mohammed V is not only a visually appealing square but also a bustling center of commerce and activity. The surrounding streets are lined with shops, cafes, and restaurants, making it a popular destination for locals and tourists alike. It is a vibrant place where people gather to socialize, shop, and experience the pulse of the city.

The square also serves as a venue for cultural events, concerts, and public gatherings. During national celebrations and holidays, the square comes alive with festivities and performances that showcase Moroccan traditions and cultural heritage.

Visiting Place Mohammed V provides a glimpse into the lively spirit and modern development of Casablanca. Whether it's enjoying a leisurely stroll, admiring the architectural landmarks, or immersing oneself in the vibrant atmosphere, the square offers a captivating experience that captures the essence of Morocco's economic and cultural capital.






