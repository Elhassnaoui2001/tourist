                            <h2 class="mb-3">General information about Casablanca</h2>
                            <p>Casablanca, the vibrant metropolis of Morocco, offers a captivating blend of 
                                historical landmarks that showcase the city's rich heritage. One of the iconic 
                                sites that visitors should not miss is the magnificent Hassan II Mosque. 
                                Standing as a testament to both faith and architectural brilliance, this mosque
                                 is adorned with intricate details and boasts breathtaking ocean views. Another 
                                 notable historical monument is the Old Medina, where narrow alleys wind their 
                                 way through bustling markets, allowing travelers to immerse themselves in the 
                                 city's authentic charm. The Casablanca Cathedral, a remnant of the colonial era,
                                  stands as an architectural gem with its neo-Gothic design and stained glass windows.
                                   For those seeking a glimpse into the city's judicial past, a visit to the Mahkama du Pacha,
                                    a beautifully restored courthouse, offers a captivating experience. As you explore 
                                    its ornate interiors, you'll be transported to a bygone era of grandeur. 
                                    These historical landmarks, along with others like Villa des Arts, Mohammed V Square, 
                                    and the picturesque Corniche, contribute to Casablanca's allure, inviting visitors to 
                                    delve into its intriguing past while embracing its vibrant present.
                            </p>
