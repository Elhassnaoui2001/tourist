                            <h4 class="mb-3">Hassan Tower</h4>
                            <img class="img-fluid w-100" src="img/hassanrabat.jpg">
                            <p>The Hassan Tower, also known as Tour Hassan, is an iconic landmark and a symbol of Rabat's rich history and architectural grandeur. Standing proudly near the Bouregreg River, this incomplete minaret is a testament to the ambitious plans of Sultan Yacoub al-Mansour, who envisioned building the largest mosque in the world during the 12th century.

The Hassan Tower was intended to be the minaret of the grand mosque, but construction came to a halt after the Sultan's death in 1199, leaving it unfinished. Despite its incomplete state, the tower still stands at an impressive height of approximately 44 meters (144 feet), showcasing the intricate decorative details and geometric patterns characteristic of Moroccan Almohad architecture.

Adjacent to the tower lies the remains of the massive mosque complex that was intended to be built. The site now serves as an open esplanade known as the Bouregreg Esplanade, providing a picturesque setting for visitors to explore the ruins and appreciate the scale of the original architectural vision.

The Hassan Tower holds a significant place in Moroccan history and is a major tourist attraction in Rabat. Visitors can climb the tower and enjoy panoramic views of the city and the nearby mausoleum of King Mohammed V. The surrounding area is beautifully landscaped, with gardens and pathways that create a serene atmosphere for leisurely strolls and contemplation.

The Hassan Tower represents the architectural prowess and the ambitious vision of the Almohad dynasty, leaving a lasting impression on all who visit. Its commanding presence, combined with its historical significance, makes it a must-visit site for those interested in Moroccan history, culture, and architecture..</p>
