                            <h2 class="mb-3">General information about Fes</h2>
<p>Fes, located in the heart of Morocco, is a city of remarkable historical and cultural significance. Often referred to as "the spiritual capital of the country," Fes is renowned for its architectural heritage, traditional craftsmanship, and captivating medina. Wandering through the labyrinthine streets of the old city, visitors can uncover treasures such as the renowned Al-Qarawiyyin University, founded in 859, which is considered the oldest continuously operating university in the world. Fes' bustling souks are brimming with vibrant colors, enchanting aromas, and unique handicrafts, ranging from intricately woven carpets to exquisite pottery. The city is also known for its refined gastronomy, with traditional Moroccan dishes that tantalize the taste buds. With its well-preserved medieval ambiance and impressive cultural heritage, Fes offers a complete immersion into the history and culture of Morocco.</p>
<p>During a visit to Fes, there are several historical and tourist landmarks that visitors should not miss. Here are some of them:</p>                               
<br>
                            
                            <h4 class="mb-3">Fes Medina:</h4>
                            <img class="img-fluid w-100" src="img/mdinafes.jpg">
<p>The Medina of Fez preserves, in an ancient part comprising numerous monumental buildings, the memory of the capital founded by the Idrisid dynasty between 789 and 808 A.D. The original town was comprised of two large fortified quarters separated by the Fez wadi: the banks of the Andalous and those of the Kaïrouanais. In the 11th century, the Almoravids reunited the town within a sole rampart and, under the dynasty of the Almohads (12th and 13th centuries), the original town (Fez el-bali) already grew to its present-day size. Under the Merinids (13th to 15th centuries), a new town (Fez Jedid) was founded (in 1276) to the west of the ancient one (Fez El-Bali). It contains the royal palace, the army headquarters, fortifications and residential areas. At that time, the two entities of the Medina of Fez evolve in symbiosis forming one of the largest Islamic metropolis's representing a great variety of architectural forms and urban landscapes. They include a considerable number of religious, civil and military monuments that brought about a multi-cultural society. This architecture is characterised by construction techniques and decoration developed over a period of more than ten centuries, and where local knowledge and skills are interwoven with diverse outside inspiration (Andalousian, Oriental and African). The Medina of Fez is considered as one of the most extensive and best conserved historic towns of the Arab-Muslim world. The unpaved urban space conserves the majority of its original functions and attribute. It not only represents an outstanding architectural, archaeological and urban heritage, but also transmits a life style, skills and a culture that persist and are renewed despite the diverse effects of the evolving modern societies.</p>
                            <h4 class="mb-3">Al-Qarawiyyin University</h4>
                            <img class="img-fluid w-100" src="img/l9arawiyin.jpg">
<p>The University of al-Qarawiyyin or al-Karaouine  is a university located in Fes, Morocco.

The al-Qarawiyyin mosque, a religious school / college was founded by Fatima al-Fihri in 859 with an associated school, or madrasa, which subsequently became one of the leading spiritual and educational centers of the historic Muslim world.

It was incorporated into Morocco’s modern state university system in 1963.

It is the oldest existing, continually operating and the first degree awarding educational institution in the world according to UNESCO and Guinness World Records and is sometimes referred to as the oldest university.

These claims are subject to discussions as other institutions, such as the Zaytouna mosque-school founded in 703 in Tunis, predated the founding of al-Qarawiyyin.

Al-Qarawiyyin itself is named after the Qairaouan Mosque in Tunisia, the oldest mosque in the Maghreb and the cradle of the Muslim Maliki rite.

Education at al-Qarawiyyin University concentrates on the Islamic religious and legal sciences with a heavy emphasis on, and particular strengths in classical Arabic grammar/linguistics and Maliki law, although a few lessons on other non-Islamic subjects such as French, English, and even IT are also offered to students.

Teaching is delivered in the traditional method, in which students are seated in a semi-circle (halqa) around a sheikh, who prompts them to read sections of a particular text, asks them questions on particular points of grammar, law, or interpretation, and explains difficult points.

Students from all over Morocco and Islamic West Africa attend the Qarawiyyin, although a few might come from as far afield as Muslim Central Asia.
</p>
